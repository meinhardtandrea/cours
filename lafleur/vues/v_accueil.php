<h1>Qui suis-je ?</h1>
<div id="accueil_presentation">
    <br>Baigné depuis l’enfance dans l’univers du végétal et fils de paysagiste, être fleuriste pour moi, est un moyen de libérer ma créativité.<br>

    <br>À l’âge de 15 ans, j’ai décidé d’apprendre le métier. De 2008 à 2012, j’ai suivi 4 années de formation en alternance, pour obtenir le CAP puis le BP.
    J’ai participé à de nombreux concours, qui m’on permis de terminer plusieurs fois sur les podiums.<br>

    <br>Après plusieurs années de formations et de salariat, c’est avec une grande fierté et satisfaction que je mets en avant mon savoir-faire au sein de ma propre entreprise.<br>

    <br>Emilien Le Blevenec <br>   
